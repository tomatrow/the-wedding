	<h1>Volunteer Opportunities</h1>
	<p>Would you like to lend a helping hand? Well, you may keep your hand, but we would love your help!</p>

	<section>
		<h2>General Setup</h2>
		<p>
			We will be setting up on Friday, and anyone may come to help us. If you are camping on Friday, don’t feel
			obligated to help; you may enjoy the park!
		</p>
	</section>

	<section>
		<h2>Specific Volunteer Roles</h2>
		<p>We have several jobs that we would like to assign to individual people. They include:</p>
		<ul>
			<li>
				<strong>Ferry Driver:</strong> The one who will remain on call to pick up anyone who must park at Silvercrest Picnic
				Area.
			</li>
			<li>
				<strong>Cauldron Tender:</strong> 2 people who will start campfires on Saturday morning, place the frozen stew into
				the cauldrons, stir and tend it until it is hot, and then put out the fire so it isn’t left unattended during the
				ceremony.
			</li>
			<li>
				<strong>Vegetable Tender:</strong> The one who will tend the mashed vegetable side dishes, similarly to the cauldrons.
			</li>
			<li><strong>Servers:</strong> 2 people who will ladle the stew into bowls.</li>
			<li>
				<strong>Keeper of the Bread:</strong> The one who will bring the bread to the campground and divide it into baskets
				to be served at the appropriate time (there will be regular and gluten-free varieties).
			</li>
			<li>
				<strong>Veggie Master:</strong> The one who will receive vegetables and hummus from our hands, turn them into beautiful
				platters, and then bring them on Saturday.
			</li>
			<li>
				<strong>Hydrator:</strong> The one who will bring 3 5-gallon jugs of drinks and a cooler full of ice, then fill the
				drink dispensers and keep them full.
			</li>
		</ul>
	</section>

	<section>
		<h2>Donations Needed</h2>
		<p>
			We also need large coolers to transport frozen food, so if you would like to donate a cooler that we may borrow,
			that would be helpful.
		</p>
	</section>
